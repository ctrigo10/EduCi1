<?php

function sumar($a,$b){
    return $a + $b;
}

function restar($a,$b){
    return $a - $b;
}

function multiplicar($a,$b){
    return $a * $b;
}

function dividir($a,$b){
    return $a / $b;
}

function perimetro($a, $b){
    return ($a + $b)*2;
}

function area($a, $b){
    return $b * $a;
}

function perimetroc($a){
    return 2 * 3.1416 * $a;
}

function areac($a){
    return 3.1416 * ($a * $a);
}